

const javaScript = {
  creator: 'Brendan Eich',
  createAt: '1995.05',
  standardName: 'ECMAScript',
  currentVersion: 2023,
};

console.dir(javaScript);
console.log(javaScript);


/*


console.log와 console.dir은 모두 자바스크립트 객체나 값을 콘솔에 출력할 때 사용하지만, 출력 방식이 다릅니다.



1. console.log()
용도: 일반적인 값을 출력하는 데 사용됩니다.
출력 형식: 객체를 콘솔에 직렬화된 형태로 출력합니다. 즉, 전체 구조가 단순하게 나타납니다.
사용 예시: 주로 변수의 값이나 상태를 확인할 때 사용합니다.


2. console.dir()
용도: 객체의 구조적 정보를 자세히 탐색할 때 사용됩니다.
출력 형식: 객체의 속성 트리 구조를 볼 수 있습니다. 특히 브라우저 콘솔에서는 객체를 펼쳐서 속성을 확인할 수 있는 방식으로 출력됩니다.
사용 예시: 복잡한 객체나 DOM 요소를 구조적으로 파악하고자 할 때 사용합니다.




*/