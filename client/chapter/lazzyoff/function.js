/*
💫 function


함수란?
함수는 특정 작업이나 계산을 수행하기 위해 정의된 코드의 블록이에요. 
함수는 반복해서 사용할 수 있고, 입력값(매개변수)을 받아서 결과값을 반환할 수 있습니다.



 함수는 function 키워드나 화살표 함수(=>)를 통해 정의됩니다. 
 예를 들어*/

 function sayHello() {
  console.log("Hello, world!");
}

sayHello(); // "Hello, world!" 출력


/* 위의 예제에서 sayHello는 함수 이름이고, console.log는 "Hello, world!"를 출력하는 코드입니다. 
sayHello()를 호출할 때마다 이 코드가 실행됩니다.






*/