/*
🎭 querySelector()


CSS 선택자를 사용해 HTML 문서에서 첫 번째로 일치하는 요소를 선택하는 JavaScript 메서드입니다. 


querySelectorAll() : querySelectorAll: 중복된 클래스가 있는 모든 요소를 선택할 때 사용하며, 
모든 일치하는 요소를 NodeList 형태로 반환합니다.





1. 클래스 선택자
const button = document.querySelector('.클래스 이름');



2. 아이디 선택자
const header = document.querySelector('#아이디 이름');



3. 태그 이름 선택자
const firstDiv = document.querySelector('div');



4. 복합 선택자
const specificButton = document.querySelector('button.submit');
<button> 요소 중 class="submit"을 가진 첫 번째 요소를 선택합니다.




 */