/*
🎭 assert()



 조건이 참인지 확인하고, 그렇지 않으면 오류를 발생시키는 함수입니다. 
 주로 코드가 예상한 대로 동작하는지 검증할 때 사용되며, 디버깅 및 테스트 과정에서 유용합니다.

assert() 함수는 조건이 참일 때는 아무 작업도 하지 않지만, 조건이 거짓일 경우 에러를 발생시킵니다. 
JavaScript에서 assert는 console.assert()로 사용됩니다.




문법:
console.assert(condition, message);
condition: 확인하고자 하는 조건입니다. 이 조건이 false일 경우, assert가 실패하고 에러 메시지가 출력됩니다.
message: 조건이 false일 때 출력할 메시지입니다.





*/